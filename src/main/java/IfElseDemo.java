public class IfElseDemo {
    public static void main(String[] args) {
        int a = 2;
        int b = 2;

        if(a > b) {
            System.out.println("a = " + a);
        }
        else if (a == b) {
            System.out.println("Hai số bằng nhau");
        }
        else {
            System.out.println("b = " + b);
        }
    }
}
